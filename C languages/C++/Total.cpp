// This program calculates the addition of two variables.
#include <iostream>
using namespace std;

int main ()
{
	// The first variable has a value of 50.
	int firstVariable = 50;
	
	// The second variable has a value of 100.
	int secondVariable = 100;
	
	// Total adds the two variables.
	int total = firstVariable + secondVariable;
	
	// Display the results.
	cout << "The value from the addition of the two variables is " << total << endl;
}
